package egovframework.example.sample.service;


import lombok.AllArgsConstructor;

@AllArgsConstructor
public class Loc{
	private String id;

	private String name;

	private String trty;

	private float lat;

	private float lng;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getTrty() {
		return trty;
	}

	public void setTrty(String trty) {
		this.trty = trty;
	}

	public float getLat() {
		return lat;
	}

	public void setLat(float lat) {
		this.lat = lat;
	}

	public float getLng() {
		return lng;
	}

	public void setLng(float lng) {
		this.lng = lng;
	}
}
